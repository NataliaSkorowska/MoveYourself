    private void jButton_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_registerActionPerformed
        
        boolean emailExists = false;
        
        String name = jTextField_name.getText();
        String surname = jTextField_surname.getText();
        String email = jTextField_email.getText();
        String pass = String.valueOf(jPasswordField_register.getPassword());
        String repass = String.valueOf(jPasswordField_retype.getPassword());
        String bdate = null;
        
        if(name.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Proszę podać imie");
        }
        else if(surname.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Proszę podać nazwisko");
        }
        else if(email.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Proszę podać email");
        }
        else if(jDateChooser_bdate.getDate() == null)
        {
            JOptionPane.showMessageDialog(null,"Proszę podać datę urodzenia");
        }
        else if (pass.equals(""))
        {
            JOptionPane.showMessageDialog(null,"Proszę podać hasło");
        }
        else if (!pass.equals(repass))
        {
            JOptionPane.showMessageDialog(null,"Podane hasła muszą być identyczne!");
        }
        else
        {
        if(jDateChooser_bdate.getDate() != null)
        {
             SimpleDateFormat dateformat = new SimpleDateFormat("yyyy-MM-dd");
             bdate = dateformat.format(jDateChooser_bdate.getDate());
        }
        Connection con;
        CallableStatement cs;
       
        try {
            con = DriverManager.getConnection("jdbc:sqlserver://localhost;databaseName=MoveYourself",
                    "s2232","s2232");
            cs = con.prepareCall("{call dbo.RejestrujKlienta(?,?,?,?,?)}");
            
            cs.setString(1, name);
            cs.setString(2,surname);
            cs.setString(3,email);
            cs.setString(4, pass);
              if(bdate !=null)
                {
                    cs.setString(5,bdate);
                }
             if(cs.executeUpdate() > 0)
                {
                JOptionPane.showMessageDialog(null,"Gratulujemy! Rejestracja przebiegła poprawnie");
                KlientGlownaFrame kgf = new KlientGlownaFrame();
                kgf.setVisible(true);
                kgf.pack();
                kgf.setLocationRelativeTo(null);
                kgf.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                this.dispose(); 
                
                }

        } catch (SQLException ex) {
             java.util.logging.Logger.getLogger(StartFrame.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }

        }   
    }//GEN-LAST:event_jButton_registerActionPerformed

